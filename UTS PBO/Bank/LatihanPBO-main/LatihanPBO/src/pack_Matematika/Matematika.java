public class Matematika {
    static void pertambahan(int a, int b){
        System.out.println(a + " + "  + b + " = " + (a+b));
    }
    
    static void pengurangan(int a, int b){
        System.out.println(a + " - "  + b + " = " + (a-b));
    }
    
    static void perkalian(int a, int b){
        System.out.println(a + " * "  + b + " = " + (a*b));
    }
    
    static void pembagian(int a, int b){
        System.out.println(a + " / "  + b + " = " + (a/b));
    }
}
